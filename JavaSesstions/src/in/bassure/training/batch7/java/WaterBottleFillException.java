package in.bassure.training.batch7.java;

public class WaterBottleFillException extends IllegalArgumentException {

    public WaterBottleFillException() {
        super();
    }

    public WaterBottleFillException(String message) {
        super(message);
    }

    public WaterBottleFillException(Throwable ex) {
        super(ex);
    }

    public WaterBottleFillException(String message, Throwable ex) {
        super(message, ex);
    }
}





